"""
nbakit.report — build a FINDINGS.md → PDF report.

The rendering engine is shared; project-specific text (title, subtitle, data
credit line, footnote, appendix description, paths) is supplied via ReportConfig.

    from nbakit.report import ReportConfig, build_report
    build_report(ReportConfig(
        title="My Analysis",
        subtitle="A deep dive",
        data_line="Data: NBA.com · 1983–84 through 2025–26 · 52,399 games",
        footnote="See Appendix A for full tables.",
        appendix_desc="Auto-generated by analysis.py each run.",
        output_path="generated/report.pdf",
        pipeline_cmd="MPLBACKEND=Agg python3 run_analysis.py",
    ))
"""

import os
import re
import sys
from dataclasses import dataclass, field
from datetime import datetime

from reportlab.lib import colors
from reportlab.lib.enums import TA_CENTER, TA_JUSTIFY
from reportlab.lib.pagesizes import letter
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import inch
from reportlab.platypus import (
    HRFlowable,
    Image,
    KeepTogether,
    PageBreak,
    Paragraph,
    Preformatted,
    SimpleDocTemplate,
    Spacer,
    Table,
    TableStyle,
)

from nbakit.mdpdf import (
    MONO,
    _draw_footer,
    _md_inline,
    _parse_regression_sections,
    _results_text,
)

PAGE_W, PAGE_H = letter
MARGIN    = 0.85 * inch
CONTENT_W = PAGE_W - 2 * MARGIN

DARK   = "#2c2c2a"
MID    = "#666666"
BLUE   = "#1f77b4"
ACCENT = BLUE


# ── Config ─────────────────────────────────────────────────────────────────────

@dataclass
class ReportConfig:
    title: str
    subtitle: str = ""
    author: str = "Justin Pietsch"
    data_line: str = ""
    footnote: str = ""
    appendix_desc: str = "Auto-generated each run."
    findings_path: str = "FINDINGS.md"
    results_path: str = "RESULTS.md"
    output_path: str = "generated/report.pdf"
    pipeline_cmd: str = "python3 analysis.py"


